# Sum up diagonals for an n x n spiral of numbers
def sum_diagonals(n):
    total = 1
    counter = 1

    for interval in range(2, n, 2):
        for j in range(4):
            counter += interval
            total += counter

    return total
    
###########################################
###          MAIN FUNCTION              ###
###########################################

print('Sum of diagonals is: %d' % sum_diagonals(1001))
